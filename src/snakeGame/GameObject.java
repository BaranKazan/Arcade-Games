package snakeGame;

/**
 * Interface for interfaces that will have common methods.
 */
public interface GameObject {
	
	/**
	 * Returns the X position of the object.
	 * @return the X value position.
	 */
	public int getX();
	/**
	 * Returns the Y position of the object.
	 * @return the Y value position.
	 */
	public int getY();
}
